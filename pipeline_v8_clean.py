    "def process_mat_file(file_path, expected_task):\n",
    "    \"\"\"\n",
    "    Process a single LabChart .mat file and compute key physiological metrics.\n",
    "    \"\"\"\n",
    "    mat, channels, info = extract_labchart_channels(file_path)\n",
    "\n",
    "    mcav = channels[\"mcav\"][\"y\"]\n",
    "    hr = channels[\"hr\"][\"y\"]\n",
    "    map_signal = channels[\"map\"][\"y\"]\n",
    "\n",
    "    if info[\"type\"] == \"block\":\n",
    "        t_mcav_abs = np.asarray(channels[\"mcav\"][\"t\"], dtype=float)\n",
    "        t_hr_abs = np.asarray(channels[\"hr\"][\"t\"], dtype=float)\n",
    "        t_map_abs = np.asarray(channels[\"map\"][\"t\"], dtype=float)\n",
    "    else:\n",
    "        t_mcav_abs = np.arange(len(mcav)) / channels[\"mcav\"][\"fs\"]\n",
    "        t_hr_abs = np.arange(len(hr)) / channels[\"hr\"][\"fs\"]\n",
    "        t_map_abs = np.arange(len(map_signal)) / channels[\"map\"][\"fs\"]\n",
    "\n",
    "    t_mcav_1hz_abs, mcav_1hz = mean_per_second(mcav, t_mcav_abs)\n",
    "    t_hr_1hz_abs, hr_1hz = mean_per_second(hr, t_hr_abs)\n",
    "    t_map_1hz_abs, map_1hz = mean_per_second(map_signal, t_map_abs)\n",
    "\n",
    "    start_time_abs, end_time_abs, comments, window_source = find_task_window_from_comments(\n",
    "        mat, expected_task\n",
    "    )\n",
    "\n",
    "    t_mcav_rel = t_mcav_1hz_abs - start_time_abs\n",
    "    t_hr_rel = t_hr_1hz_abs - start_time_abs\n",
    "    t_map_rel = t_map_1hz_abs - start_time_abs\n",
    "\n",
    "    hr_1hz_aligned = np.interp(t_mcav_rel, t_hr_rel, hr_1hz)\n",
    "    map_1hz_aligned = np.interp(t_mcav_rel, t_map_rel, map_1hz)\n",
    "\n",
    "    n = min(len(t_mcav_rel), len(mcav_1hz), len(hr_1hz_aligned), len(map_1hz_aligned))\n",
    "\n",
    "    df_1hz = pd.DataFrame({\n",
    "        \"t\": t_mcav_rel[:n],\n",
    "        \"mcav\": mcav_1hz[:n],\n",
    "        \"hr\": hr_1hz_aligned[:n],\n",
    "        \"map\": map_1hz_aligned[:n],\n",
    "    })\n",
    "\n",
    "    df_1hz[\"cvci\"] = df_1hz[\"mcav\"] / df_1hz[\"map\"]\n",
    "\n",
    "    mcav_baseline = mean_in_window(df_1hz, \"mcav\", *BASELINE_WINDOW)\n",
    "    map_baseline = mean_in_window(df_1hz, \"map\", *BASELINE_WINDOW)\n",
    "    cvci_baseline = mcav_baseline / map_baseline if not np.isnan(map_baseline) and map_baseline != 0 else np.nan\n",
    "\n",
    "    max_available_time = float(df_1hz[\"t\"].max())\n",
    "    analysis_end = min(ANALYSIS_WINDOW[1], max_available_time)\n",
    "\n",
    "    response_df = df_1hz[\n",
    "        (df_1hz[\"t\"] >= ANALYSIS_WINDOW[0]) & (df_1hz[\"t\"] <= analysis_end)\n",
    "    ].copy()\n",
    "\n",
    "    if response_df.empty or analysis_end < MIN_RESPONSE_WINDOW_S:\n",
    "        raise ValueError(\"Insufficient response window after alignment.\")\n",
    "\n",
    "    peak_mcav = float(response_df[\"mcav\"].max())\n",
    "    peak_idx = response_df[\"mcav\"].idxmax()\n",
    "    ttp = float(response_df.loc[peak_idx, \"t\"])\n",
    "\n",
    "    map_at_peak = float(response_df.loc[peak_idx, \"map\"])\n",
    "    cvci_peak = peak_mcav / map_at_peak if map_at_peak != 0 else np.nan\n",
    "\n",
    "    delta_mcav_pct = (\n",
    "        ((peak_mcav - mcav_baseline) / mcav_baseline) * 100\n",
    "        if not np.isnan(mcav_baseline) and mcav_baseline != 0\n",
    "        else np.nan\n",
    "    )\n",
    "    delta_map_pct = (\n",
