    "def mean_per_second(signal, time):\n",
    "    \"\"\"Compute per-second mean values from a continuous signal.\"\"\"\n",
    "    duration = int(np.floor(time[-1]))\n",
    "    means = []\n",
    "    times = []\n",
    "\n",
    "    for second in range(duration + 1):\n",
    "        idx = (time >= second) & (time < second + 1)\n",
    "        if np.any(idx):\n",
    "            means.append(np.mean(signal[idx]))\n",
    "            times.append(second)\n",
    "\n",
    "    return np.array(times), np.array(means)\n",
    "\n",
