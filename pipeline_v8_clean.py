    "def extract_comments(mat):\n",
    "    \"\"\"\n",
    "    Extract event/comment markers from LabChart .mat structures.\n",
    "\n",
    "    Returns a sorted list of tuples: (time, text)\n",
    "    \"\"\"\n",
    "    comments = []\n",
    "\n",
    "    possible_pairs = [\n",
    "        (\"comtime\", \"comtext\"),\n",
    "        (\"commenttime\", \"commenttext\"),\n",
    "        (\"eventtime\", \"eventtext\"),\n",
    "    ]\n",
    "\n",
    "    for time_key, text_key in possible_pairs:\n",
    "        if time_key in mat and text_key in mat:\n",
    "            times = np.atleast_1d(mat[time_key])\n",
    "            texts = np.atleast_1d(mat[text_key])\n",
    "\n",
    "            n = min(len(times), len(texts))\n",
    "            for i in range(n):\n",
    "                try:\n",
    "                    t = float(times[i])\n",
    "                    txt = normalize_text(texts[i])\n",
    "                    comments.append((t, txt))\n",
    "                except Exception:\n",
    "                    pass\n",
    "\n",
    "    if \"comtick_block1\" in mat and \"comtext_block1\" in mat and \"ticktimes_block1\" in mat:\n",
    "        comtick = np.atleast_1d(mat[\"comtick_block1\"])\n",
    "        comtext = np.atleast_1d(mat[\"comtext_block1\"])\n",
    "        ticktimes = np.atleast_1d(mat[\"ticktimes_block1\"]).squeeze()\n",
    "        ticktimes = np.ravel(ticktimes)\n",
    "\n",
    "        n = min(len(comtick), len(comtext))\n",
    "        for i in range(n):\n",
    "            try:\n",
    "                idx = int(comtick[i]) - 1\n",
    "                if 0 <= idx < len(ticktimes):\n",
    "                    t = float(ticktimes[idx])\n",
    "                    txt = normalize_text(comtext[i])\n",
    "                    comments.append((t, txt))\n",
    "            except Exception:\n",
    "                pass\n",
    "\n",
    "    comments = sorted(comments, key=lambda x: x[0])\n",
    "    return comments\n",
