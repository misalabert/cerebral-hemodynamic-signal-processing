    "\n",
    "    task_comments = [(t, txt) for t, txt in comments if txt == expected_task]\n",
    "\n",
    "    if len(task_comments) >= 2:\n",
    "        start_time_abs = task_comments[0][0]\n",
    "        end_time_abs = task_comments[1][0]\n",
    "        source = \"comments\"\n",
    "        return start_time_abs, end_time_abs, comments, source\n",
    "\n",
    "    elif len(task_comments) == 1:\n",
    "        start_time_abs = task_comments[0][0]\n",
