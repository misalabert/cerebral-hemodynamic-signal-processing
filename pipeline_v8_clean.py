    "        source = \"single_comment\"\n",
    "        return start_time_abs, end_time_abs, comments, source\n",
    "\n",
    "    else:\n",
    "        start_time_abs = 20.0\n",
    "        end_time_abs = 80.0\n",
    "        source = \"fallback_20_80\"\n",
